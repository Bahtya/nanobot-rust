/// Tracks whether a client has successfully authenticated.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum AuthState {
    /// Client has not yet authenticated; waiting for first message or token.
    Pending,
    /// Client is fully authenticated.
    Authenticated,
}


/// WebSocket channel adapter — a server that browser chat clients connect to.
///
/// Each connected browser tab is tracked as a separate client with a unique
/// UUID-based `client_id`. The `client_id` is used as `chat_id` in the
/// message bus, so each browser session gets its own agent conversation.
pub struct WebSocketChannel {
    /// Address to listen on (e.g. "127.0.0.1:8090").
    listen_addr: String,
    /// Whether the server is running.
    connected: bool,
    /// Handler to forward inbound messages to the bus.
    message_handler: Option<mpsc::Sender<InboundMessage>>,
    /// Running flag for graceful shutdown.
    running: Arc<AtomicBool>,
    /// Connected clients: client_id → outbound sender.
    clients: Arc<DashMap<String, mpsc::UnboundedSender<String>>>,
    /// Whether token authentication is required.
    auth_required: bool,
    /// Expected auth token (if auth is required).
    auth_token: Option<String>,
}

impl WebSocketChannel {
    /// Create a new WebSocket channel with the default listen address and no auth.
    pub fn new() -> Self {
        Self {
            listen_addr: DEFAULT_LISTEN_ADDR.to_string(),
            connected: false,
            message_handler: None,
            running: Arc::new(AtomicBool::new(false)),
            clients: Arc::new(DashMap::new()),
            auth_required: false,
            auth_token: None,
        }
    }

    /// Create with a custom listen address (for testing or custom config).
    pub fn with_addr(addr: String) -> Self {
        Self {
            listen_addr: addr,
            connected: false,
            message_handler: None,
            running: Arc::new(AtomicBool::new(false)),
            clients: Arc::new(DashMap::new()),
            auth_required: false,
            auth_token: None,
        }
    }

    /// Create with authentication enabled.
    ///
    /// When `auth_required` is true, clients must authenticate via query
    /// parameter `?token=xxx` or by sending `{"type":"auth","token":"xxx"}`
    /// as their first message.
    pub fn with_auth(addr: String, auth_required: bool, token: Option<String>) -> Self {
        Self {
            listen_addr: addr,
            connected: false,
            message_handler: None,
            running: Arc::new(AtomicBool::new(false)),
            clients: Arc::new(DashMap::new()),
            auth_required,
            auth_token: token,
        }
    }

    /// Number of currently connected clients.
    pub fn client_count(&self) -> usize {
        self.clients.len()
    }

    /// Extract a token from the WebSocket request's query string.
    ///
    /// Looks for `?token=xxx` or `&token=xxx` in the query portion of the URL.
    #[allow(dead_code)]
    fn extract_query_token(_request: &tungstenite::handshake::client::Request) -> Option<String> {
        // tungstenite's accept_async doesn't expose the request directly,
        // so we use a custom accept that captures it.
        // For now, return None — the actual extraction happens in accept_with_token.
        None
    }

    /// Perform the WebSocket handshake and extract any `?token=` from the request.
    async fn accept_with_token(
        stream: tokio::net::TcpStream,
    ) -> Result<(
        tokio_tungstenite::WebSocketStream<tokio::net::TcpStream>,
        Option<String>,
    )> {
        let ws_stream = tokio_tungstenite::accept_async(stream).await?;
        Ok((ws_stream, None))
    }

    /// Run the accept loop — binds a TCP listener and accepts WebSocket
    /// connections until `running` is cleared.
    async fn run_accept_loop(
        listener: TcpListener,
        handler: mpsc::Sender<InboundMessage>,
        running: Arc<AtomicBool>,
        clients: Arc<DashMap<String, mpsc::UnboundedSender<String>>>,
        auth_required: bool,
        auth_token: Option<String>,
    ) {
        use futures::StreamExt;

        info!(
            "WebSocket server listening on {}",
            listener.local_addr().expect("listener was just bound")
        );

        while running.load(Ordering::Relaxed) {
            // Accept with timeout so we can check the running flag.
            let accept =
                tokio::time::timeout(std::time::Duration::from_secs(1), listener.accept()).await;

            let (stream, addr) = match accept {
                Ok(Ok((s, a))) => (s, a),
                Ok(Err(e)) => {
                    error!("WebSocket accept error: {}", e);
                    continue;
                }
                Err(_) => continue, // timeout — check running flag
            };

            let (ws_stream, query_token) = match Self::accept_with_token(stream).await {
                Ok(ws) => ws,
                Err(e) => {
                    warn!("WebSocket handshake failed from {}: {}", addr, e);
                    continue;
                }
            };

            let client_id = uuid::Uuid::new_v4().to_string();

            // Determine initial auth state.
            let (initial_state, send_welcome) = if !auth_required {
                (AuthState::Authenticated, true)
            } else if let Some(ref token) = query_token {
                if auth_token.as_deref() == Some(token.as_str()) {
                    (AuthState::Authenticated, true)
                } else {
                    // Token from query string didn't match — reject immediately.
                    info!(
                        "WebSocket client {} from {} failed query-token auth",
                        client_id, addr
                    );
                    // We can't send on a ws_stream that we're about to split,
                    // so just drop it.
                    continue;
                }
            } else {
                // Auth required but no token in query — wait for auth message.
                (AuthState::Pending, false)
            };

            info!(
                "WebSocket client connected: {} from {} (auth={:?})",
                client_id, addr, initial_state
            );

            let (client_tx, client_rx) = mpsc::unbounded_channel::<String>();

            // Send welcome message if already authenticated.
            if send_welcome {
                let welcome = WsEnvelope::welcome(&client_id);
                if let Ok(json) = welcome.to_json() {
                    if client_tx.send(json).is_err() {
                        warn!("Failed to send welcome to {}", client_id);
                    }
                }
            }

            clients.insert(client_id.clone(), client_tx);

            let (sink, stream) = ws_stream.split();

            // Spawn tasks for this client.
            let read_handler = handler.clone();
            let read_client_id = client_id.clone();
            let read_clients = clients.clone();
            let read_running = running.clone();
            let read_auth_token = auth_token.clone();

            let write_client_id = client_id.clone();
            let write_clients = clients.clone();
            let write_running = running.clone();

            tokio::spawn(async move {
                Self::write_loop(
                    sink,
                    client_rx,
                    write_client_id,
                    write_clients,
                    write_running,
                )
                .await;
            });

            tokio::spawn(async move {
                Self::read_loop(
                    stream,
                    read_client_id,
                    read_handler,
                    read_clients,
                    read_running,
                    initial_state,
                    read_auth_token,
                )
                .await;
            });
        }

        info!("WebSocket accept loop stopped");
    }

    /// Read loop for a single client — parses inbound messages and forwards
    /// them to the message bus handler.
    ///
    /// Handles authentication flow: if `auth_state` is `Pending`, the first
    /// message must be an `{"type":"auth","token":"xxx"}` envelope.
    async fn read_loop(
        stream: futures::stream::SplitStream<
            tokio_tungstenite::WebSocketStream<tokio::net::TcpStream>,
        >,
        client_id: String,
        handler: mpsc::Sender<InboundMessage>,
        clients: Arc<DashMap<String, mpsc::UnboundedSender<String>>>,
        running: Arc<AtomicBool>,
        mut auth_state: AuthState,
        auth_token: Option<String>,
    ) {
        use futures::StreamExt;
        use tokio_tungstenite::tungstenite::Message as WsMessage;

        let mut stream = stream;

        while let Some(msg_result) = stream.next().await {
            if !running.load(Ordering::Relaxed) {
                break;
            }

            let msg = match msg_result {
                Ok(WsMessage::Text(text)) => text,
                Ok(WsMessage::Close(_)) => {
                    debug!("WebSocket client {} closed connection", client_id);
                    break;
                }
                Ok(WsMessage::Ping(_data)) => {
                    // tungstenite auto-replies with pong
                    continue;
                }
                Ok(_) => continue,
                Err(e) => {
                    warn!("WebSocket read error for {}: {}", client_id, e);
                    break;
                }
            };

            // Try to parse as a generic JSON value first.
            let raw_value: serde_json::Value = match serde_json::from_str(&msg) {
                Ok(v) => v,
                Err(e) => {
                    debug!(
                        "WebSocket invalid JSON from {}: {} — raw: {}",
                        client_id, e, msg
                    );
                    continue;
                }
            };

            // --- Auth gate: if pending, only accept auth messages ---
            if auth_state == AuthState::Pending {
                if raw_value.get("type").and_then(|v| v.as_str()) == Some("auth") {
                    let token = raw_value.get("token").and_then(|v| v.as_str());
                    if let (Some(provided), Some(expected)) = (token, auth_token.as_deref()) {
                        if provided == expected {
                            auth_state = AuthState::Authenticated;
                            info!("WebSocket client {} authenticated", client_id);
                            // Send welcome now that auth succeeded.
                            let welcome = WsEnvelope::welcome(&client_id);
                            if let Ok(json) = welcome.to_json() {
                                if let Some(client_tx) = clients.get(&client_id) {
                                    let _ = client_tx.send(json);
                                }
                            }
                            continue;
                        }
                    }
                    // Auth failed — send error and close.
                    warn!("WebSocket client {} auth failed", client_id);
                    let err = WsEnvelope::error("auth_required", "Authentication failed");
                    if let Ok(json) = err.to_json() {
                        if let Some(client_tx) = clients.get(&client_id) {
                            let _ = client_tx.send(json);
                        }
                    }
                    break;
                }
                // Not an auth message while pending — reject.
                let err = WsEnvelope::error(
                    "auth_required",
                    "Authentication required. Send {\"type\":\"auth\",\"token\":\"...\"} first.",
                );
                if let Ok(json) = err.to_json() {
                    if let Some(client_tx) = clients.get(&client_id) {
                        let _ = client_tx.send(json);
                    }
                }
                break;
            }

            // Detect format: envelope has "type", legacy has "role" + "content".
            let (content_text, envelope_trace_id) = if raw_value.get("type").is_some() {
                // New envelope format.
                let envelope: WsEnvelope = match serde_json::from_value(raw_value.clone()) {
                    Ok(e) => e,
                    Err(e) => {
                        debug!(
                            "WebSocket invalid envelope from {}: {} — raw: {}",
                            client_id, e, msg
                        );
                        continue;
                    }
                };

                match envelope.msg_type.as_str() {
                    "ping" => {
                        // Respond with pong.
                        let pong = WsEnvelope::pong();
                        if let Ok(json) = pong.to_json() {
                            if let Some(client_tx) = clients.get(&client_id) {
                                let _ = client_tx.send(json);
                            }
                        }
                        continue;
                    }
                    "message" => (
                        envelope.content.clone().unwrap_or_default(),
                        envelope.trace_id.clone(),
                    ),
                    _ => {
                        // Ignore unknown envelope types.
                        debug!(
                            "WebSocket unknown envelope type '{}' from {}",
                            envelope.msg_type, client_id
                        );
                        continue;
                    }
                }
            } else if raw_value.get("role").is_some() {
                // Legacy {role, content} format — backward compat.
                let legacy: WsInboundMessage = match serde_json::from_value(raw_value) {
                    Ok(m) => m,
                    Err(e) => {
                        debug!(
                            "WebSocket invalid legacy message from {}: {} — raw: {}",
                            client_id, e, msg
                        );
                        continue;
                    }
                };
                (legacy.content, None)
            } else {
                debug!(
                    "WebSocket unrecognized message format from {}: {}",
                    client_id, msg
                );
                continue;
            };

            // Skip empty messages.
            if content_text.is_empty() {
                continue;
            }

            let message_type = if content_text.starts_with('/') {
                MessageType::Command
            } else {
                MessageType::Text
            };

            let source = SessionSource {
                platform: Platform::WebSocket,
                chat_id: client_id.clone(),
                chat_name: None,
                chat_type: "dm".to_string(),
                user_id: Some(client_id.clone()),
                user_name: None,
                thread_id: None,
                chat_topic: None,
            };

            let metadata = HashMap::new();

            // Generate or adopt trace_id for full-chain tracing.
            let trace_id = envelope_trace_id.unwrap_or_else(|| {
                format!(
                    "kst_ws_{}_{}",
                    &client_id[..8.min(client_id.len())],
                    &uuid::Uuid::new_v4().to_string()[..8]
                )
            });

            let inbound = InboundMessage {
                channel: Platform::WebSocket,
                sender_id: client_id.clone(),
                chat_id: client_id.clone(),
                content: content_text,
                media: vec![],
                metadata,
                source: Some(source),
                message_type,
                message_id: None,
                trace_id: Some(trace_id),
                reply_to: None,
                timestamp: chrono::Local::now(),
            };

            if let Err(e) = handler.send(inbound).await {
                warn!(
                    "Failed to forward WebSocket message from {}: {}",
                    client_id, e
                );
            }
        }

        // Client disconnected — clean up.
        clients.remove(&client_id);
        info!("WebSocket client disconnected: {}", client_id);
    }

    /// Write loop for a single client — forwards outbound messages to the
    /// WebSocket sink.
    async fn write_loop(
        sink: futures::stream::SplitSink<
            tokio_tungstenite::WebSocketStream<tokio::net::TcpStream>,
            tokio_tungstenite::tungstenite::Message,
        >,
        mut rx: mpsc::UnboundedReceiver<String>,
        client_id: String,
        clients: Arc<DashMap<String, mpsc::UnboundedSender<String>>>,
        running: Arc<AtomicBool>,
    ) {
        use futures::SinkExt;
        use tokio_tungstenite::tungstenite::Message as WsMessage;

        let mut sink = sink;

        while running.load(Ordering::Relaxed) {
            let msg = match rx.recv().await {
                Some(m) => m,
                None => break,
            };

            if let Err(e) = sink.send(WsMessage::Text(msg.into())).await {
                warn!("WebSocket write error for {}: {}", client_id, e);
                break;
            }
        }

        // Clean up.
        clients.remove(&client_id);
        let _ = sink.close().await;
        debug!("WebSocket write loop ended for {}", client_id);
    }
}

impl Default for WebSocketChannel {
    fn default() -> Self {
        Self::new()
    }
}

#[async_trait]
impl BaseChannel for WebSocketChannel {
    fn name(&self) -> &str {
        "websocket"
    }

    fn platform(&self) -> Platform {
        Platform::WebSocket
    }

    fn is_connected(&self) -> bool {
        self.connected
    }

    /// Bind the TCP listener and start the WebSocket accept loop.
    async fn connect(&mut self) -> Result<bool> {
        let listener = TcpListener::bind(&self.listen_addr).await?;
        info!("WebSocket channel bound to {}", self.listen_addr);

        self.connected = true;
        self.running.store(true, Ordering::Relaxed);

        if let Some(handler) = self.message_handler.clone() {
            let running = self.running.clone();
            let clients = self.clients.clone();
            let listen_addr = listener.local_addr()?.to_string();
            let auth_required = self.auth_required;
            let auth_token = self.auth_token.clone();
            tokio::spawn(async move {
                Self::run_accept_loop(
                    listener,
                    handler,
                    running,
                    clients,
                    auth_required,
                    auth_token,
                )
                .await;
                info!("WebSocket server on {} stopped", listen_addr);
            });
            info!("WebSocket accept loop spawned on {}", self.listen_addr);
        } else {
            warn!("WebSocket channel has no message handler set; not starting accept loop");
        }

        Ok(true)
    }

    /// Stop the server and disconnect all clients.
    async fn disconnect(&mut self) -> Result<()> {
        self.running.store(false, Ordering::Relaxed);
        self.clients.clear();
        self.connected = false;
        info!("WebSocket channel disconnected");
        Ok(())
    }

    /// Send a text message to a specific client using envelope format.
    async fn send_message(
        &self,
        chat_id: &str,
        content: &str,
        reply_to: Option<&str>,
    ) -> Result<SendResult> {
        self.send_message_with_trace(chat_id, content, reply_to, None)
            .await
    }

    /// Send a text message with trace context for request-response correlation.
    async fn send_message_with_trace(
        &self,
        chat_id: &str,
        content: &str,
        reply_to: Option<&str>,
        trace_id: Option<&str>,
    ) -> Result<SendResult> {
        let client = match self.clients.get(chat_id) {
            Some(c) => c,
            None => {
                return Ok(SendResult {
                    success: false,
                    message_id: None,
                    error: Some(format!("WebSocket client '{}' not connected", chat_id)),
                    retryable: false,
                });
            }
        };

        let mut envelope = WsEnvelope::message(content);
        envelope.trace_id = trace_id.map(|t| t.to_string());
        envelope.reply_to = reply_to.map(|r| r.to_string());
        let json = match envelope.to_json() {
            Ok(j) => j,
            Err(e) => {
                return Ok(SendResult {
                    success: false,
                    message_id: None,
                    error: Some(format!("Failed to serialize message: {}", e)),
                    retryable: false,
                });
            }
        };

        match client.send(json) {
            Ok(()) => {
                debug!("Sent message to WebSocket client {}", chat_id);
                Ok(SendResult {
                    success: true,
                    message_id: Some(format!("ws_{}", chat_id)),
                    error: None,
                    retryable: false,
                })
            }
            Err(e) => {
                warn!("Failed to send to WebSocket client {}: {}", chat_id, e);
                Ok(SendResult {
                    success: false,
                    message_id: None,
                    error: Some(format!("WebSocket send error: {}", e)),
                    retryable: true,
                })
            }
        }
    }

    /// Send a typing indicator to a specific client.
    async fn send_typing(&self, chat_id: &str) -> Result<()> {
        if let Some(client) = self.clients.get(chat_id) {
            let msg = WsTypingIndicator { r#type: "typing" };
            let json = serde_json::to_string(&msg)?;
            let _ = client.send(json);
        }
        Ok(())
    }

    /// Send an image to a specific client using envelope format.
    async fn send_image(
        &self,
        chat_id: &str,
        image_url: &str,
        caption: Option<&str>,
    ) -> Result<SendResult> {
        let client = match self.clients.get(chat_id) {
            Some(c) => c,
            None => {
                return Ok(SendResult {
                    success: false,
                    message_id: None,
                    error: Some(format!("WebSocket client '{}' not connected", chat_id)),
                    retryable: false,
                });
            }
        };

        let msg = WsImageMessage {
            r#type: "image",
            url: image_url,
            caption,
        };
        let json = serde_json::to_string(&msg)?;

        match client.send(json) {
            Ok(()) => Ok(SendResult {
                success: true,
                message_id: Some(format!("ws_img_{}", chat_id)),
                error: None,
                retryable: false,
            }),
            Err(e) => Ok(SendResult {
                success: false,
                message_id: None,
                error: Some(format!("WebSocket image send error: {}", e)),
                retryable: true,
            }),
        }
    }

    fn set_message_handler(&mut self, handler: mpsc::Sender<InboundMessage>) {
        self.message_handler = Some(handler);
    }
}

